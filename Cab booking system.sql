create table Bookings(booking_id int Primary Key, user_id int, driver_id int, pickup varchar(100), drop_location varchar(100),fare decimal(10,2),booking_time datetime,status varchar(20));
create table Payment(payment_id int Primary Key,booking_id int,amount decimal(10,2),payment_method varchar(20),payment_status varchar(20));
ALTER TABLE Customer MODIFY user_id INT;
ALTER TABLE Driver MODIFY driver_id INT;

